    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String file1="",file2="";
        int x=Valor();
        if(x==1){
            file1="DT\\Diag1.txt";
            file2="DT\\Tra1.txt";
        }
        if(x==2){
            file1="DT\\Diag2.txt";
            file2="DT\\Tra2.txt";
        }
        if(x==3){
            file1="DT\\Diag3.txt";
            file2="DT\\Tra3.txt";
        }
        if(x==4){
            file1="DT\\Diag4.txt";
            file2="DT\\Tra4.txt";
        }
        try {
            BufferedReader bf1 = new BufferedReader(new FileReader(file1));
            BufferedReader bf2 = new BufferedReader(new FileReader(file2));
            String cadena,res1="",res2="";
            try {
                while((cadena=bf1.readLine())!=null){
                    //System.out.println(""+cadena);
                    res1+=cadena+" ";
                    
                }
                 while((cadena=bf2.readLine())!=null){
                    //System.out.println(""+cadena);
                    res2+=cadena+" ";
                }
                 jTextArea1.setText(res1);
                 jTextArea2.setText(res2);
            } catch (IOException ex) {
                Logger.getLogger(Diagnostico.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Diagnostico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

